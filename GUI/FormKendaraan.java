    private void tbKendaraanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbKendaraanMouseClicked
        try {
            int baris = tbKendaraan.getSelectedRow();
            crud.bukaKoneksi();
            k = crud.readById(Integer.parseInt(tbKendaraan.getValueAt(baris, 0).toString()));
            crud.tutupKoneksi();
            txtId.setText(Integer.toString(k.getId()));
            txtJenis.setText(k.getJenis());
            txtMerk.setText(k.getMerk());
            txtTipe.setText(k.getTipe());
            txtThn_Produksi.setText(k.getThn_produksi());
            txtBhn_Bakar.setText(k.getBhn_bakar());
            txtKecepatan.setText(k.getKecepatan());
            txtKapasitas.setText(k.getKapasitas());
            txtMesin.setText(k.getMesin());
            txtHarga.setText(k.getHarga());
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Ambil data gagal");
        }
    }//GEN-LAST:event_tbKendaraanMouseClicked

    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        Kendaraan k;
        if (status.equals("add")) {
            try {
                if (txtJenis.getText().isEmpty() || txtMerk.getText().isEmpty()
                        || txtTipe.getText().isEmpty() || txtThn_Produksi.getText().isEmpty() || txtBhn_Bakar.getText().isEmpty() || txtKecepatan.getText().isEmpty()
                        || txtKapasitas.getText().isEmpty() || txtMesin.getText().isEmpty() || txtHarga.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Data harus diisi semua");
                } else {
                    crud.bukaKoneksi();
                    k = new Kendaraan();
                    k.setJenis(txtJenis.getText());
                    k.setMerk(txtMerk.getText());
                    k.setTipe(txtTipe.getText());
                    k.setThn_produksi(txtThn_Produksi.getText());
                    k.setBhn_bakar(txtBhn_Bakar.getText());
                    k.setKecepatan(txtKecepatan.getText());
                    k.setKapasitas(txtKapasitas.getText());
                    k.setMesin(txtMesin.getText());
                    k.setHarga(txtHarga.getText());
                    crud.create(k);
                    getData();
                    readData();
                    crud.tutupKoneksi();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }
        } else {
            try {
                if (txtJenis.getText().isEmpty() || txtMerk.getText().isEmpty()
                        || txtTipe.getText().isEmpty() || txtThn_Produksi.getText().isEmpty() || txtBhn_Bakar.getText().isEmpty() || txtKecepatan.getText().isEmpty()
                        || txtKapasitas.getText().isEmpty() || txtMesin.getText().isEmpty() || txtHarga.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Data harus diisi semua");
                } else {
                    crud.bukaKoneksi();
                    k = new Kendaraan();
                    k.setId(Integer.parseInt(txtId.getText()));
                    k.setJenis(txtJenis.getText());
                    k.setMerk(txtMerk.getText());
                    k.setTipe(txtTipe.getText());
                    k.setThn_produksi(txtThn_Produksi.getText());
                    k.setBhn_bakar(txtBhn_Bakar.getText());
                    k.setKecepatan(txtKecepatan.getText());
                    k.setKapasitas(txtKapasitas.getText());
                    k.setMesin(txtMesin.getText());
                    k.setHarga(txtHarga.getText());
                    crud.update(k);
                    getData();
                    readData();
                    crud.tutupKoneksi();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }
        }
    }//GEN-LAST:event_btSimpanActionPerformed

