    public void getData() {
        try {
            model.setRowCount(0);
            crud.bukaKoneksi();
            ArrayList<Kendaraan> list = crud.read();
            System.out.println(list.size());
            for (Kendaraan k : list) {
                model.addRow(new Object[]{
                    k.getId(),
                    k.getJenis(),
                    k.getMerk(),
                    k.getTipe(),
                    k.getThn_produksi(),
                    k.getBhn_bakar(),
                    k.getKecepatan(),
                    k.getKapasitas(),
                    k.getMesin(),
                    k.getHarga()
                });
            }
            crud.tutupKoneksi();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Gagal Mengambil Data" + ex.getMessage());
        }
    }

