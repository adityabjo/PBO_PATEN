    public void getData() {
        try {
            model.setRowCount(0);
            crud.bukaKoneksi();
            ArrayList<Pembeli> list = crud.read();
            System.out.println(list.size());
            for (Pembeli p : list) {
                model.addRow(new Object[]{
                    p.getId(),
                    p.getNo_identitas(),
                    p.getNama(),
                    p.getTgl_pembelian(),
                    p.getId_barang(),
                    p.getJumlah(),
                    p.getTot_harga(),
                    p.getDp(),
                    p.getStatus(),
                    p.getBonus(),});
            }
            crud.tutupKoneksi();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Gagal Mengambil Data");
        }
    }

