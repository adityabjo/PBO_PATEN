    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        Kendaraan k;
        if (status.equals("add")) {
            try {
                if (txtNo_Identitas.getText().isEmpty() || txtNama.getText().isEmpty()
                        || txtTgl_Pembelian.getText().isEmpty() || txtId_Barang.getText().isEmpty() || txtJumlah.getText().isEmpty() || txtTot_harga.getText().isEmpty()
                        || txtDp.getText().isEmpty() || txtStatus.getText().isEmpty() || txtBonus.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Data harus diisi semua");
                } else {
                    crud.bukaKoneksi();
                    p = new Pembeli();
                    p.setNo_identitas(txtNo_Identitas.getText());
                    p.setNama(txtNama.getText());
                    p.setTgl_pembelian(txtTgl_Pembelian.getText());
                    p.setId_barang(txtId_Barang.getText());
                    p.setJumlah(txtJumlah.getText());
                    p.setTot_harga(txtTot_harga.getText());
                    p.setDp(txtDp.getText());
                    p.setStatus(txtStatus.getText());
                    p.setBonus(txtBonus.getText());
                    crud.create(p);
                    getData();
                    readData();
                    crud.tutupKoneksi();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }
        } else {
            try {
                if (txtNo_Identitas.getText().isEmpty() || txtNama.getText().isEmpty()
                        || txtTgl_Pembelian.getText().isEmpty() || txtId_Barang.getText().isEmpty() || txtJumlah.getText().isEmpty() || txtTot_harga.getText().isEmpty()
                        || txtDp.getText().isEmpty() || txtStatus.getText().isEmpty() || txtBonus.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Data harus diisi semua");
                } else {
                    crud.bukaKoneksi();
                    p = new Pembeli();
                    p.setId(Integer.parseInt(txtId.getText()));
                    p.setNo_identitas(txtNo_Identitas.getText());
                    p.setNama(txtNama.getText());
                    p.setTgl_pembelian(txtTgl_Pembelian.getText());
                    p.setId_barang(txtId_Barang.getText());
                    p.setJumlah(txtJumlah.getText());
                    p.setTot_harga(txtTot_harga.getText());
                    p.setDp(txtDp.getText());
                    p.setStatus(txtStatus.getText());
                    p.setBonus(txtBonus.getText());
                    crud.update(p);
                    getData();
                    readData();
                    crud.tutupKoneksi();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }
        }
    }                                        

